let day = 3;

switch (day) {
  case 1:
    console.log("monday");
    break;

  case 2:
    console.log("tuesday");
    break;

  case 3:
    console.log("wednesday");
    break;

  case 4:
    console.log("thursday");
    break;

  case 5:
    console.log("friday");
    break;

  default:
    console.log("Holiday");
}

